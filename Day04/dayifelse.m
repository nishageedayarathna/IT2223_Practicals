day = input('Enter the day(1-7): ');

if day==1
    disp('Monday!');
elseif day==2
    disp('Tuesday!');
elseif day==3
    disp('Wednesday!');
elseif day==4
    disp('Thursday!');
elseif day==5
    disp('Friday!');
elseif day==6
    disp('Saturday!');
elseif day==7
    disp('Sunday!');
else
    disp('Invalid number!');
end