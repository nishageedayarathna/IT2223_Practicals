marks=input('Enter your marks : ')

if marks>100 || marks<0
    disp('Invalid mark')
elseif marks>=90
    disp('Your grade is A+')
elseif marks>=80
    disp('Your grade is A')
elseif marks>=70
    disp('Your grade is B')
elseif marks>=60
    disp('Your grade is C')
elseif marks>=50
    disp('Your grade is D')
else
    disp('Your grade is F')
end